    New-Shortcut (Join-Path $publicDesktop 'DFIR PowerShell.lnk') "$env:WINDIR\System32\WindowsPowerShell\v1.0\powershell.exe" '-NoExit -Command "dfir-lab"' 'C:\dfir\lab'
    Write-Host '[shortcuts] Desktop shortcuts created.'
} catch { Write-Warning "[shortcuts] shortcut creation issue: $($_.Exception.Message)" }

exit 0
