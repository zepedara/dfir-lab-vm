# ---- DFIR Lab helpers (auto-generated by dfir-lab-vm) ----
function dfir-aio {
    <#
      Launch the offline dfir-aio:v2 Linux toolbox in WSL2, mounting a folder as /data.
      Usage:
        dfir-aio                  # mounts the current directory as /data, opens the shell
        dfir-aio C:\dfir\lab\module-01-prefetch-pecmd\data
        dfir-aio . PECmd -d /data --csv /data   # run one tool non-interactively
    #>
    param(
        [string]$DataDir = (Get-Location).Path,
        [Parameter(ValueFromRemainingArguments=$true)] $Cmd
    )
    $full = (Resolve-Path $DataDir).Path
    $wsl  = (wsl -d Ubuntu -- wslpath "$full").Trim()
    $tail = if ($Cmd) { ($Cmd -join ' ') } else { '' }
    # Ensure docker is up, then run the container.
    wsl -d Ubuntu -u root -- bash -lic "service docker start >/dev/null 2>&1 || (pgrep dockerd >/dev/null 2>&1 || (dockerd >/var/log/dockerd.log 2>&1 &)); sleep 2" | Out-Null
    wsl -d Ubuntu -- bash -lic "docker run -it --rm -v '$wsl':/data dfir-aio:v2 $tail"
}

function dfir-lab   { Set-Location 'C:\dfir\lab' ; Get-ChildItem -Directory | Select-Object Name }
function dfir-wsl   { wsl -d Ubuntu }
Set-Alias dfir dfir-lab

Write-Host "DFIR lab ready. Try:  dfir-lab   |   dfir-aio   |   PECmd --help" -ForegroundColor Cyan
# ---- end DFIR Lab helpers ----
'@
Set-Content -Path $profilePath -Value $profileBody -Encoding UTF8
