# ------------------------------ Shortcuts -----------------------------------
function New-Shortcut($lnk, $target, $arguments, $workdir) {
    $ws = New-Object -ComObject WScript.Shell
    $s = $ws.CreateShortcut($lnk)
    $s.TargetPath = $target
    if ($arguments) { $s.Arguments = $arguments }
    if ($workdir)   { $s.WorkingDirectory = $workdir }
    $s.Save()
}
